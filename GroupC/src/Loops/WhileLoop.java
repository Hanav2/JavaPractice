package Loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		// This help to print out 0-15
		
		int i =1;
		while (i<=15) {
			
			//	 i++; this will add one before the print out 2-16 
			
			System.out.println(i);
			i++; // this will add one one while printing out 1-15
		}

	}

}
 //prefix  x=2; x++, it will put it as 2 but will prepare 2+1=3 for the next step 
// postfix x=2;++x will be 3. but if it continue from the first step 3+1=4.